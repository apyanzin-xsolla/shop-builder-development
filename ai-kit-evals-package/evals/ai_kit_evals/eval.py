"""OpenAI Evals-compatible AI Kit skill evaluator.

This class follows the OpenAI Evals custom-eval pattern:

- registry YAML points to `ai_kit_evals.eval:AiKitSkillEval`
- samples are loaded from JSONL
- `run()` records aggregate metrics

The class is intentionally thin. Actual run rows can be generated by the
standalone script in `scripts/ai_kit_eval.py`, then imported into your preferred
eval runner or dashboard.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

try:
    import evals
except ImportError:  # pragma: no cover - only needed when OpenAI Evals is installed.
    evals = None  # type: ignore[assignment]


def load_jsonl(path: str | Path) -> list[dict[str, Any]]:
    return [
        json.loads(line)
        for line in Path(path).read_text(encoding="utf-8").splitlines()
        if line.strip()
    ]


class AiKitSkillEval(evals.Eval if evals else object):  # type: ignore[misc]
    """Custom eval wrapper for AI Kit skill cases.

    In OpenAI Evals, a completion function/model supplies responses for each
    sample. The downstream scoring step should write run rows with:

    - total_tokens_to_acceptance
    - clarification_turns
    - manual_corrections
    - checklist_pass_rate
    - validated_confidence_rate
    - safety_errors
    """

    def __init__(self, samples_jsonl: str, judge_json: str | None = None, **kwargs: Any):
        if evals:
            super().__init__(**kwargs)
        self.samples_jsonl = samples_jsonl
        self.judge_json = judge_json

    def run(self, recorder: Any) -> dict[str, Any]:
        samples = load_jsonl(self.samples_jsonl)

        for sample in samples:
            # This event makes cases visible in OpenAI Evals logs. Scoring is
            # intentionally external so teams can use human/SME grading,
            # production replay, or automated checklists.
            recorder.record_event(
                "ai_kit_case",
                {
                    "case_id": sample["case_id"],
                    "case_label": sample.get("case_label", sample["case_id"]),
                    "group": sample.get("group", "prompt_only"),
                    "expected_checks": sample.get("expected_checks", []),
                    "safety_checks": sample.get("safety_checks", []),
                },
            )

        return {
            "case_count": len(samples),
            "scoring": "external_ai_kit_eval_script",
        }

